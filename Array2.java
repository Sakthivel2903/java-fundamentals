public class Array2 {
    public static void main(String[] args) {
		int a[][] = new int[3][2];
		a[0][0]	= 56;
		a[0][1]	= 74;
		a[1][0]	= 88;
		a[1][1]	= 34;
		a[2][0]	= 24;
		a[2][1]	= 43;
		for (int i = 0; i < 3 ; i ++){
			for (int j = 0; j < 2 ; j ++){
				System.out.print(a[i][j]+ " ");
			} System.out.println(); }		
	}}


